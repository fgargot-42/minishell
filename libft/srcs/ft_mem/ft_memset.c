#include <sys/types.h>

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*d;

	d = (char *)s;
	i = 0;
	while (i < n)
	{
		d[i] = c;
		i++;
	}
	return (s);
}
