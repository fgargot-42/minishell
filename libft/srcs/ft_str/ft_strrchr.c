char	*ft_strrchr(const char *s, int c)
{
	const char	*res;

	res = 0;
	while (*s)
	{
		if (*s == (char)c)
			res = s;
		s++;
	}
	if (*s == '\0' && (char)c == 0)
		res = s;
	return ((char *)res);
}
